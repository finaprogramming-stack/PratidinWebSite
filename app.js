function updateSliderState() {
  if (!servicesGrid) return;
  if (window.innerWidth > 980) {
    document.body.classList.remove("services-at-start", "services-at-end");
    return;
  }
  const maxScroll = servicesGrid.scrollWidth - servicesGrid.clientWidth;
  document.body.classList.toggle("services-at-start", servicesGrid.scrollLeft <= 8);
  document.body.classList.toggle("services-at-end", servicesGrid.scrollLeft >= maxScroll - 8);
}

function scrollServices(direction) {
  if (!servicesGrid || !serviceCards.length) return;
  const distance = serviceCards[0].getBoundingClientRect().width + 18;
  servicesGrid.scrollBy({ left: distance * direction, behavior: "smooth" });
}

async function submitForm(form) {
  const dict = translations[currentLang] || translations.ru;
  const button = form.querySelector('button[type="submit"]');
  const feedback = form.querySelector(".form-feedback");
  const originalText = button.dataset.originalText || button.textContent;
  button.dataset.originalText = originalText;
  button.disabled = true;
  button.textContent = dict.submitting;
  feedback.textContent = "";
  feedback.classList.remove("is-error", "is-success");

  const payload = new FormData(form);
  payload.append("form_type", form.dataset.formType || form.id || "general");
  payload.append("lang", currentLang);

  try {
    const response = await fetch(form.action, {
      method: "POST",
      body: payload,
      headers: { "X-Requested-With": "XMLHttpRequest" }
    });
    const result = await response.json().catch(() => null);
    if (!response.ok || !result.success) {
      throw new Error(result?.message || dict.error);
    }
    feedback.textContent = result.message || dict.success;
    feedback.classList.add("is-success");
    form.reset();
  } catch (error) {
    feedback.textContent = error.message || dict.networkError;
    feedback.classList.add("is-error");
  } finally {
    button.disabled = false;
    button.textContent = originalText;
  }
}

