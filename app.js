function updateAnchorOffset() {
  if (!header) return;
  const headerHeight = Math.ceil(header.getBoundingClientRect().height);
  document.documentElement.style.setProperty("--anchor-offset", `${headerHeight + 8}px`);
}

function renderWidget(name) {
  const items = (widgets[currentLang] || widgets.ru)[name];
  const index = widgetState[name];
  const title = document.getElementById(`${name}-title`);
  const text = document.getElementById(`${name}-text`);
  const badge = document.querySelector(`#widget-${name} .widget-badge`);
  title.textContent = items[index].title;
  text.textContent = items[index].text;
  badge.textContent = `${index + 1} / ${items.length}`;
}

function animateWidgetChange(name, nextIndex) {
  const widgetContent = document.querySelector(`#widget-${name} .widget-content`);
  if (!widgetContent || widgetContent.dataset.animating === "true") return;
  widgetContent.dataset.animating = "true";
  widgetContent.classList.remove("is-entering");
  widgetContent.classList.add("is-switching");
  window.setTimeout(() => {
    widgetState[name] = nextIndex;
    renderWidget(name);
    widgetContent.classList.remove("is-switching");
    widgetContent.classList.add("is-entering");
    window.setTimeout(() => {
      widgetContent.classList.remove("is-entering");
      widgetContent.dataset.animating = "false";
    }, 340);
  }, 220);
}

function openModal(key) {
  const content = (modalContent[currentLang] || modalContent.ru)[key];
  if (!content) return;
  modalEyebrow.textContent = content.eyebrow;
  modalTitle.textContent = content.title;
  modalBody.innerHTML = content.body;
  modalBackdrop.dataset.activeModal = key;
  modalBackdrop.classList.add("active");
  modalBackdrop.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
}

function closeModal() {
  modalBackdrop.classList.remove("active");
  modalBackdrop.setAttribute("aria-hidden", "true");
  modalBackdrop.dataset.activeModal = "";
  document.body.classList.remove("modal-open");
}

function setLanguage(lang) {
  currentLang = translations[lang] ? lang : "ru";
  const dict = translations[currentLang];
  localStorage.setItem("pratidin-lang", currentLang);
  document.documentElement.lang = dict.pageLang;
  document.documentElement.dataset.lang = currentLang;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dict[key]) node.innerHTML = dict[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (dict[key]) node.setAttribute("placeholder", dict[key]);
  });

  document.querySelectorAll("[data-i18n-title]").forEach((node) => {
    const key = node.dataset.i18nTitle;
    if (dict[key]) {
      node.setAttribute("title", dict[key]);
      node.setAttribute("aria-label", dict[key]);
    }
  });

  languageButtons.forEach((button) => {
    const active = button.dataset.lang === currentLang;
    button.classList.toggle("active", active);
    button.setAttribute("aria-pressed", String(active));
  });

  const prevArrow = document.querySelector(".services-arrow-left");
  const nextArrow = document.querySelector(".services-arrow-right");
  if (languageSwitcher) languageSwitcher.setAttribute("aria-label", dict.languageSwitcherLabel);
  if (languageTrigger) languageTrigger.setAttribute("aria-label", dict.languageSwitcherLabel);
  if (languageCurrent) languageCurrent.textContent = currentLang.toUpperCase();
  if (navToggle) {
    navToggle.setAttribute("aria-label", document.body.classList.contains("nav-open") ? dict.menuCloseLabel : dict.menuOpenLabel);
  }
  if (prevArrow) prevArrow.setAttribute("aria-label", dict.sliderPrev);
  if (nextArrow) nextArrow.setAttribute("aria-label", dict.sliderNext);
  if (modalClose) modalClose.setAttribute("aria-label", dict.modalCloseLabel);

  renderWidget("advantage");
  renderWidget("savings");

  if (modalBackdrop.dataset.activeModal) openModal(modalBackdrop.dataset.activeModal);
}

function closeLanguageMenu() {
  if (!languageSwitcher || !languageTrigger) return;
  languageSwitcher.classList.remove("is-open");
  languageTrigger.setAttribute("aria-expanded", "false");
  const menu = languageSwitcher.querySelector(".lang-menu");
  if (menu) menu.setAttribute("aria-hidden", "true");
}

function toggleLanguageMenu() {
  if (!languageSwitcher || !languageTrigger) return;
  const willOpen = !languageSwitcher.classList.contains("is-open");
  languageSwitcher.classList.toggle("is-open", willOpen);
  languageTrigger.setAttribute("aria-expanded", String(willOpen));
  const menu = languageSwitcher.querySelector(".lang-menu");
  if (menu) menu.setAttribute("aria-hidden", String(!willOpen));
}

